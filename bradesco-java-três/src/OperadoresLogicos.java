public class OperadoresLogicos {
    public static void main(String[] args) {
        
        boolean condicao1 = true;
        boolean condicao2 = false;
        
        if(condicao1 && condicao2){
            System.out.println("As duas condições são verdadeiras"); // -----> AND no Python
        }
        if(condicao1 || condicao2){
            System.out.println("Uma das duas condições é verdadeira");// ----> OR no Python

        System.out.println("Fim");
        }    
    }
}
// && Operador Lógico "E"
// || Operador Lógico "OU"